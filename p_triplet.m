function p = p_triplet(a, b, c)
    % p_triplet: computes the product abc of the pythagorean triplet (a, b, c)
    %     Signature: p = p_triplet(a, b, c)
    %     Input a: a natural
    %     Input b: a natural
    %     Input c: a natural, satisfying a^2 + b^2 = c^2
    %     Output p: the product abc
    
    a = a;
    b = b;
    c = c;
end
